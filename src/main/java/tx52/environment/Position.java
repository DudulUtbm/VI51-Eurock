package tx52.environment;

class Position {

		private float x;
		private float y;

		Position(float x2, float y2) {
			
			this.x = x2;
			this.y = y2;
		}

		public float getX() {
			return x;
		}

		public void setX(float x) {
			this.x = x;
		}

		public float getY() {
			return y;
		}

		public void setY(float y) {
			this.y = y;
		}

	}

